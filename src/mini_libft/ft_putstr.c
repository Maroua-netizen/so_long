#include "../../so_long.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		*count += 6;
	}
	else
	{
		i = 0;
		while (s[i])
		{
			write(1, &s[i], 1);
			(*count)++;
			i++;
		}
	}
}
